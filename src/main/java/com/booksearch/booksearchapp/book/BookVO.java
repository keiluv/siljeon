package com.booksearch.booksearchapp.book;

public class BookVO {
	String bookname;
	String bookgenre;
	String bookcompany;
	int bookprice;
	String bookrepertoire;
	String bookfeat;
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	public String getBookgenre() {
		return bookgenre;
	}
	public void setBookgenre(String bookgenre) {
		this.bookgenre = bookgenre;
	}
	public String getBookcompany() {
		return bookcompany;
	}
	public void setBookcompany(String bookcompany) {
		this.bookcompany = bookcompany;
	}
	public int getBookprice() {
		return bookprice;
	}
	public void setBookprice(int bookprice) {
		this.bookprice = bookprice;
	}
	public String getBookrepertoire() {
		return bookrepertoire;
	}
	public void setBookrepertoire(String bookrepertoire) {
		this.bookrepertoire = bookrepertoire;
	}
	public String getBookfeat() {
		return bookfeat;
	}
	public void setBookfeat(String bookfeat) {
		this.bookfeat = bookfeat;
	}
}
